public class Carteira {
    private float saldo;
    private Usuario usuario;




     public Carteira() {
        this.saldo = 0;
        this.usuario = usuario;
    }


    public float getSaldo() {
        return saldo;
    }


    public Usuario getUsuario() {
        return usuario;
    }


    public void setSaldo(float saldo) {
        this.saldo = saldo;
    }

}
